
/* ------------------ EXERCÍCIO 04 -------------------- 
  Fazer um programa que leia o número de um funcionário, seu número 
  de horas trabalhadas, o valor que recebe por ora e calcula o salário 
  desse funcionário. A seguir, mostre o número e o salário do funcionário, 
  com duas casas decimais.
   ---------------------------------------------------	
*/

package exercicioEstruturaSequencial;

import java.util.Locale;
import java.util.Scanner;

public class exercicio04 {

	public static void main(String[] args) {
		
		Locale.setDefault(Locale.US);
		Scanner sc = new Scanner(System.in);
		
		int numFunc = sc.nextInt();
		int horasTrab = sc.nextInt();
		double valorHora = sc.nextDouble();
		double salario = horasTrab * valorHora;
		
		System.out.println("NUMBER = " + numFunc);
		System.out.printf("SALARY = U$ %.2f%n", salario);
		
		sc.close();
		
	}

}
